DROP TABLE IF EXISTS atividade_mensal;
DROP TABLE IF EXISTS clientes;

CREATE TABLE clientes (
    id_cliente          VARCHAR(10) PRIMARY KEY,
    nome                VARCHAR(100),
    segmento            VARCHAR(20),    -- Premium | Standard
    plano               VARCHAR(20),    -- Basic | Pro | Enterprise
    estado              CHAR(2),
    canal_aquisicao     VARCHAR(20),    -- Online | Comercial | Indicação
    data_cadastro       DATE,
    data_primeiro_pedido DATE,
    data_ultimo_pedido  DATE,
    total_pedidos       INT,
    receita_total       DECIMAL(12,2),
    ticket_medio        DECIMAL(10,2),
    meses_ativo         INT,
    status_cliente      VARCHAR(20)     -- Ativo | Em Risco | Churned
);

CREATE TABLE atividade_mensal (
    id_cliente              VARCHAR(10),
    ano_mes                 VARCHAR(7),   -- formato: 'YYYY-MM'
    pedidos_mes             INT,
    receita_mes             DECIMAL(10,2),
    dias_desde_ultimo_pedido INT,
    nps_score               INT,          -- NULL quando sem atividade
    PRIMARY KEY (id_cliente, ano_mes)
);

